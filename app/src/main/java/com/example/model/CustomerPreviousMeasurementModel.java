package com.example.model;

public class CustomerPreviousMeasurementModel {

    private String name;
    private String age;
    private String sex;
    private String height;
    private String weight;
    private String frontUrl;
    private String sideUrl;
    private String userId;
    private String date;
    private String measurement_type;
    private String oid;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getFronturl() {
        return frontUrl;
    }

    public void setFronturl(String fronturl) {
        this.frontUrl = fronturl;
    }

    public String getSideurl() {
        return sideUrl;
    }

    public void setSideurl(String sideurl) {
        this.sideUrl = sideurl;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String id) {
        this.userId = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getMeasurement_type() {
        return measurement_type;
    }

    public void setMeasurement_type(String measurement_type) {
        this.measurement_type = measurement_type;
    }

    public String getOid() {
        return oid;
    }

    public void setOid(String oid) {
        this.oid = oid;
    }
}
